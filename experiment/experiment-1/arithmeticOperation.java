class arithmeticOperation
{
    public static void main(String []args)
    {
        int a=10;
        int b=5;

        int sum =a+b;
        int difference =a-b;
        int product =a*b;
        int quotient =a/b;

        System.out.println("Sum of the two integer: " + sum);
        System.out.println("Difference of the two integer: " + difference);
        System.out.println("Product of the two interger: "+ product);
        System.out.println("Quotient of the two integer: "+ quotient);
    }
}